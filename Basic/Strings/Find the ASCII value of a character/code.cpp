#include <bits/stdc++.h>

using namespace std;

class ASCII {

  public:

    void value() {

      char c = 'A';
      cout << "The ASCII value of " << c << " is " << int(c);
    }
};
int main() {

  ASCII p1;
  p1.value();

  return 0;
}

// output The ASCII value of A is 65 ,     Time Complexity: O(1)  Space Complexity: O(1)